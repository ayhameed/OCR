% --- Executes on key press with focus on edit2 and none of its controls.
function edit2_KeyPressFcn(hObject, eventdata, handles)
% hObject    handle to edit2 (see GCBO)
% eventdata  structure with the following fields (see MATLAB.UI.CONTROL.UICONTROL)
%	Key: name of the key that was pressed, in lower case
%	Character: character interpretation of the key(s) that was pressed
%	Modifier: name(s) of the modifier key(s) (i.e., control, shift) pressed
% handles    structure with handles and user data (see GUIDATA)
password=get(handles.edit2,'Userdata');
key=get(gcf,'currentkey');

switch key
    case 'backspace'
        password=password(1:length(password)-1); % delete the last character
    case 'return'
        gui =getappdata(0,'logindlg');
        OK([],[],gui.main);
    case 'tab'
         gui =getappdata(0,'logindlg');
       uicontrol(gui.OK);
    case 'escape'
        Close the login dialog
        Escape(fig,[])
    otherwise
        password=[password get(gcf,'CurrentCharacter')]; %add the typed character to the password
        SizePass=size(password); %find the no of asterisk
        
        if SizePass(2) > 0
            asterisk(1,1:SizePass(2))='*'; %create a string of asterisks d same as d password
            set(handles.edit2,'String',asterisk)  %replae text wit aster
        else
            set(handles.edit2,'string','');
        end
end
        set(handles.edit2, 'Userdata',password);


